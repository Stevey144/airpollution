<p class="col-sm-12 lead" style="margin-bottom: 12px;">
Air pollution is a major concern of new civilized world, which has a serious toxicological impact on human health and the environment. It has a number of different emission sources, but motor vehicles and industrial processes contribute the major part of air pollution. According to the World Health Organization, six major air pollutants include particle pollution, ground-level ozone, carbon monoxide, sulfur oxides, nitrogen oxides, and lead. Long and short term exposure to air suspended toxicants has a different toxicological impact on human including respiratory and cardiovascular diseases, neuropsychiatric complications, the eyes irritation, skin diseases, and long-term chronic diseases such as cancer. Several reports have revealed the direct association between exposure to the poor air quality and increasing rate of morbidity and mortality mostly due to cardiovascular and respiratory diseases. Air pollution is considered as the major environmental risk factor in the incidence and progression of some diseases such as asthma, lung cancer, ventricular hypertrophy, Alzheimer’s and Parkinson’s diseases, psychological complications, autism, retinopathy, fetal growth, and low birth weight. In this review article, we aimed to discuss toxicology of major air pollutants, sources of emission, and their impact on human health. We have also proposed practical measures to reduce air pollution in Iran.  </p>
